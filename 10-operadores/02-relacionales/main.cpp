#include <iostream>

using namespace std;

int main() {
	int x = 3;
	int y = 2;

	cout << "Igualdad: " << (x == y) << endl;
	cout << "Diferencia: " << (x != y) << endl;
	cout << "Menor que: " << (x < y) << endl;
	cout << "Mayor que: " << (x > y) << endl;
	cout << "Menor o igual: " << (x <= y) << endl;
	cout << "Mayor o igual: " << (x >= y) << endl;

	return 0;
}